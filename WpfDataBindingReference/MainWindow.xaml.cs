    /// <summary>
    /// Interação lógica para MainWindow.xam
    /// </summary>
    public partial class MainWindow : Window
    {
        public MainWindow()
        {
            InitializeComponent();
            this.DataContext = this;
            //we assign the "this" reference to the DataContext,
            //    which basically just tells the Window that we want itself to be the data context.
            //In the XAML, we use this fact to bind to several of the Window properties, including 
            //    Title, Width and Height. Since the window has a DataContex
        }

        private void btnUpdateSource_Click(object sender, RoutedEventArgs e)
        {
            BindingExpression binding = txtWindowTitle.GetBindingExpression(TextBox.TextProperty);
            binding.UpdateSource();
        }

    }
